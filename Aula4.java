public class Aula4 {
	
	public static void main(String[] args) {
		float v1 = 10.5;

		float v2 = 23.3;

		float soma = (v2 + v1);
		float subt = (v2 - v1);
		float mult = (v2 * v1);
		float divs = (v2 / v1); // 2.3       float / double
 
		System.out.println("Soma: " + soma);
		System.out.println("Subtração: " + subt);
		System.out.println("Multiplicação: " + mult);
		System.out.println("Divisão: " + divs);
	}
}